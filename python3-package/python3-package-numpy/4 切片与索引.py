import numpy as np


def main() -> None:
    """
    NumPy 切片与索引操作示例：
    包括基础索引、切片、布尔索引、花式索引等。
    """
    # 创建一个二维数组
    arr = np.array([[1, 2, 3], [4, 5, 6]])

    # 基础索引：访问单个元素
    print("访问第0行第1列的元素:", arr[0, 1])  # 输出 2

    # 切片操作：获取子数组
    print("\n获取所有行的第1列及之后的元素:\n", arr[:, 1:])  # [[2 3], [5 6]]

    # 获取前两行和前两列
    print("\n获取前两行和前两列:\n", arr[:2, :2])

    # 布尔索引：根据条件筛选元素
    print("\n筛选大于3的元素:\n", arr[arr > 3])

    # 花式索引：使用整数数组进行索引
    print("\n使用花式索引获取第0行和第1行的第2列元素:", arr[[0, 1], [2, 2]])  # [3, 6]

    # 获取特定行列组合的数据
    print("\n获取指定行列组合的元素:\n", arr[[0, 1]][:, [0, 2]])  # 第0和1行，第0和2列


if __name__ == "__main__":
    main()
